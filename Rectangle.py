class Rectangle:
	def __init__(self, height, width, x, y):
		self.height = height
		self.width = width
		self.x = 0
		self.y = 0
		
	def getHeight(self):
		return (self.height)


	def getWidth(self):
		return (self.width)

	def setY(self, y):
		self.y = y

	def setX(self, x):
		self.x = x

	def getX(self):
		return (self.x)

	def getY(self):
		return (self.y)